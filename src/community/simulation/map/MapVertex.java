package community.simulation.map;

public class MapVertex {
    float x, y, z;
    
    
    MapVertex(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    

    public float getX() {
        return x;
    }
    

    public void setX(float x) {
        this.x = x;
    }
    

    public float getY() {
        return y;
    }
    

    public void setY(float y) {
        this.y = y;
    }
    

    public float getZ() {
        return z;
    }
    

    public void setZ(float z) {
        this.z = z;
    }
    
    
}
